const int T = 1e7;
LL f( LL x ){
	int pos = x / T - 1;
	LL ans = P[pos];
	LL lim = T * (( x / T ));
	for( LL i = x ; i > lim ; i-- ){
		ans = ( ans * i ) % MOD;
	}
	return ans ;
}

int main(){
	LL n , m , k ;
	cin >> n >> m >> k ;
	int x = 0 , y = 0; 
	LL res = 1LL;
	LL l = 1 , r = n ;
	LL mid ;
	while( l <= r ){
		mid = ( l + r ) >> 1 ;
		if( mid <= k ) { l = mid + 1; x++;}
		else { r = mid - 1 ; y ++; }
	}
	for( int i = m - x + 1 ; i <= m ; i ++ ){
		res *= i ;
		res %= MOD; 
	}
	for( int i = n - m - y + 1 ; i <= n - m ; i ++ ){
		res *= i ;
		res %= MOD; 
	}
	LL c3 ;
	if( n - x - y < 1e7 ){
		c3 = 1;
		for( int i = 2;  i <= n - x - y ; i++ ){
			c3 = c3 * i;
			c3 %= MOD;
		}
	}else{
		c3 = f( n - x - y );		
	}
	res = res * c3 % MOD;
	cout << res << endl;
	return 0;
}